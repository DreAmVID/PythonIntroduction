#Comandos / Sintaxes básicas para Python

#listas

#Cria uma lista

lista=[1,2,3,4,5,"David"]

print(lista)

#Adiciona um valor em uma lista

lista.append("Castro")

print(lista)

#Retorna em que posição se encontra o valor

lista.index("Castro")

#Conta quantas vezes o valor aparece

lista.count(2)

#Remove um valor

lista.remove(5)

print(lista)

#Inverte a ordem da lista

lista.reverse()

print(lista)

lista2=[2,3,9,4,1,0,10]

print(lista2)

#Ordena a lista em ordem crescente

lista2.sort()

print(lista2)



